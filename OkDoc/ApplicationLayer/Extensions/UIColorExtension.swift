import UIKit.UIColor

extension UIColor {
    static var purple: UIColor {
        return UIColor(red:125/255.0, green:76/255.0, blue:245/255.0, alpha: 1)
    }
    
    static var darkPurple: UIColor {
        return UIColor(red: 125/255, green: 76/255, blue: 245/255, alpha: 1)
    }
    
    static var lightPurple: UIColor {
        return UIColor(red: 156/255, green: 141/255, blue: 255/255, alpha: 1)
    }
}
